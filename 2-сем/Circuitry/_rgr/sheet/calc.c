#include <math.h>

int main(){
	/* 0000 0000 0000 0000 */
	printf("%b\n", 1024);	/* 0001 0000 0000 0000 0x1000 */
	printf("0%b\n", 1023);	/* 0001 0000 0000 0000 0x1000 */
	printf("%b\n", 64);	/* 0000 0000 0100 0000 0x0040 */
	printf("%b\n", 4096);	/* 0001 0000 0000 0000 0x1000 */ /* 512 */
	printf("%b\n", 8192);	/* 1111 1010 0000 0000 0xfa00 */
	printf("%b\n", 32768);
	printf("%b\n", 63488); /* 1111 1000 0000 0000 0xf800 */
	printf("%b\n", 65535); /* 1111 1111 1111 1111 0xffff */
	printf("%b\n", 2048); /* 1111 1111 1111 1111 0xffff */

	return 0;
}
